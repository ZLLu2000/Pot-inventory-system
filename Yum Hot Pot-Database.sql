CREATE TABLE SUPPLIER
(SUPPLIER_ID CHAR(10),
SUPPLIER_NAME VARCHAR2(35) NOT NULL,
SUPPLIER_ADDRESS VARCHAR2(100),
     CONSTRAINT SUPPLIER_SUPPLIER_ID_PK PRIMARY KEY (SUPPLIER_ID));

CREATE TABLE MANAGER
(MANAGER_ID CHAR(5),
MANAGER_NAME VARCHAR2(20) NOT NULL,
PHONE_NUM CHAR(25),
     CONSTRAINT MANAGER_MANAGER_ID_PK PRIMARY KEY (MANAGER_ID));

CREATE TABLE STORE
(STORE_ID CHAR(5),
STORE_ADDRESS VARCHAR2(100) NOT NULL,
MANAGER_ID CHAR(5),
     CONSTRAINT STORE_STORE_ID_PK PRIMARY KEY (STORE_ID),
     CONSTRAINT STORE_MANAGER_ID_FK FOREIGN KEY (MANAGER_ID) REFERENCES MANAGER (MANAGER_ID));

CREATE TABLE PURCHASE_ORDER
(PURCHASE_ID CHAR(10),
PURCHASE_DATE DATE,
ITEM_ID CHAR(10),
ITEM_NAME VARCHAR2(20) NOT NULL,
ITEM_QUANTITY NUMBER(10,0),
UNIT_PRICE CHAR(5),
SUPPLIER_ID CHAR(10),
STORE_ID CHAR(5),
     CONSTRAINT P_O_PURCHASE_ID_PK PRIMARY KEY (PURCHASE_ID),
     CONSTRAINT P_O_SUPPLIER_ID_FK FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER (SUPPLIER_ID),
     CONSTRAINT P_O_STORE_ID_FK FOREIGN KEY (STORE_ID) REFERENCES STORE (STORE_ID));

CREATE TABLE ITEM
(ITEM_ID CHAR(10),
ITEM_NAME VARCHAR2(20) NOT NULL,
DESCRIPTIONS VARCHAR2(35),
UNIT_PRICE CHAR(5),
ENTRY_DATE DATE,
DUE_DATE DATE,
SUPPLIER_ID CHAR(10),
     CONSTRAINT ITEM_ITEM_ID_PK PRIMARY KEY (ITEM_ID),
     CONSTRAINT ITEM_SUPPLIER_ID_FK FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER (SUPPLIER_ID));

CREATE TABLE ACCOUNT_PAYABLE
(PAYABLE_ID CHAR(5),
PAYABLE_DATE DATE,
PAYABLE_AMOUNT NUMBER(10,0),
SUPPLIER_ID CHAR(10),
     CONSTRAINT A_P_PAYABLE_ID_PK PRIMARY KEY (PAYABLE_ID),
     CONSTRAINT A_P_SUPPLIER_ID_FK FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER (SUPPLIER_ID));

CREATE TABLE RAW_MATERIAL
(RM_ID CHAR(10),
RM_NAME VARCHAR2(20) NOT NULL,
REORDER_POINT CHAR(10),
ORDER_QUANTITY CHAR(10),
QUANTITY_ON_HAND CHAR(10),
     CONSTRAINT R_M_RM_ID_PK PRIMARY KEY (RM_ID));

CREATE TABLE ACCOUNT_RECEIVABLE
(RECEIVABLE_ID CHAR(5),
RECEIVABLE_DATE DATE,
RECEIVABLE_AMOUNT NUMBER(10,0),
STORE_ID CHAR(5),
     CONSTRAINT A_R_RECEIVABLE_ID_PK PRIMARY KEY (RECEIVABLE_ID),
     CONSTRAINT A_R_STORE_ID_FK FOREIGN KEY (STORE_ID) REFERENCES STORE (STORE_ID));

CREATE TABLE CUSTOMER
(CUSTOMER_ID CHAR(10),
FIRST_NAME VARCHAR2(20),
LAST_NAME VARCHAR2(10) NOT NULL,
VIP_LEVEL CHAR(5),
    CONSTRAINT CUSTOMER_CUSTOMER_ID_PK PRIMARY KEY (CUSTOMER_ID));
     
CREATE TABLE SALES_ORDER
(SALES_ID CHAR(10),
SALES_DATE DATE,
AMOUNT_PAYABLE CHAR(10),
AMOUNT_ACTUALLY_PAID NUMBER(10,0),
CUSTOMER_ID CHAR(10),
STORE_ID CHAR(5),
     CONSTRAINT S_O_SALES_ID_PK PRIMARY KEY (SALES_ID),
     CONSTRAINT S_O_CUSTOMER_ID_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID),
     CONSTRAINT S_O_STORE_ID_FK FOREIGN KEY (STORE_ID) REFERENCES STORE (STORE_ID));

CREATE TABLE PRODUCT
(PRODUCT_ID CHAR(10),
PRODUCT_NAME VARCHAR2(35) NOT NULL,
PRODUCT_PRICE CHAR(5),
RM_ID CHAR(10),
     CONSTRAINT PRODUCT_PRODUCT_ID_PK PRIMARY KEY (PRODUCT_ID),
     CONSTRAINT PRODUCT_RM_ID_FK FOREIGN KEY (RM_ID) REFERENCES RAW_MATERIAL (RM_ID));

CREATE TABLE RM_PO
(RM_ID CHAR(10),
PURCHASE_ID CHAR(10),
    CONSTRAINT R_M_RM_ID_P_O_PURCHASE_ID PRIMARY KEY (RM_ID, PURCHASE_ID));

CREATE TABLE STORE_PRODUCT
